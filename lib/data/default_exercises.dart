  static List<Exercise> getByCategory(String category) {
    return exercises.where((exercise) => exercise.category == category).toList();
  }

  static List<String> get categories {
    return exercises.map((e) => e.category).toSet().toList()..sort();
  }

  static Exercise? findByName(String name) {
    try {
      return exercises.firstWhere((e) => e.name.toLowerCase() == name.toLowerCase());
    } catch (e) {
      return null;
    }
  }
